                        Apa Itu JavaScript?

                    </h3>
                    <br>

                    <p>
                        Tahukah kamu bahwa JavaScript adalah bahasa pemrograman yang digunakan dalam pengembangan
                        website agar lebih dinamis dan interaktif. Kalau sebelumnya kamu hanya mengenal HTML dan CSS,
                        nah sekarang kamu jadi tahu bahwa JavaScript dapat meningkatkan fungsionalitas pada halaman web.
                        Bahkan dengan JavaScript ini kamu bisa membuat aplikasi, tools, atau bahkan game pada web.
                    </p>
                    <br>
                    <p>
                        Bicara teknis, JavaScript atau kita singkat menjadi JS merupakan bahasa pemrograman jenis
                        interpreter, sehingga kamu tidak memerlukan compiler untuk menjalankannya. JavaScript memiliki
                        fitur-fitur seperti berorientasi objek, client-side, high-level programming, dan loosely typed.
                    </p>
                    <br>
                    <h3>Sejarah JavaScript</h3>
                    <br>


                    <p>
                        Bagi kamu yang masih awam, mungkin penasaran bagaimana awal mulanya diciptakan bahasa
                        pemrograman ini.

                    </p>
                    <br>
                    <p>
                        Pada tahun 1994 JavaScript mulai dikenal, pada saat itu web dan internet sudah mulai berkembang.
                        JavaScript didesain oleh Brendan Eich yang merupakan karyawan Netscape. Transformasi nama
                        JavaScript, dimulai dari Mocha, Mona, LiveScript, hingga akhirnya resmi bernama JavaScript.
                    </p>
                    <br>
                    <p>
                        Versi awal bahasa JS hanya dipakai di kalangan Netscape beserta dengan fungsionalitas pun yang
                        masih terbatas. Singkat cerita pada tahun 1996 JavaScript secara resmi dinamakan sebagai
                        ECMAScript. ECMAScript 2 dikembangkan pada tahun 1998 yang dilanjutkan dengan ECMAScript 3
                        setahun kemudian. ECMAScript terus dikembangkan sampai akhirnya menjadi JavaScript atau JS
                        hingga saat ini. Pada tahun 2016, 92% web diketahui telah menggunakan JavaScript. Itulah mengapa
                        JavaScript atau JS terus berkembang.

                    </p>
                    <br>
                    <h3> Fungsi JavaScript </h3>
                    <br>
                    <p>
                        Kamu pasti sudah paham tentunya dengan fungsi JavaScript. Selain membuat web jadi lebih dinamis
                        dan interaktif, JS digunakan juga untuk proses logika data. “Intinya, fungsi JS tidak hanya soal
                        urusan front end, tapi juga sekaligus dipakai untuk urusan back end,” imbuh developer.
                    </p>
                    <br>
                    <h3>
                        Keunggulan JavaScript
                    </h3>
                    <br>





                    <p>
                        Sepertinya kamu sudah mengetahui keunggulan dari JavaScript, tapi saya akan paparkan lebih dalam
                        lagi deh hehe. Inilah beberapa keunggulan JS yang membuat tekadmu kuat untuk mempelajarinya.
                    </p>
                    <br>
                    <h4>
                        1. General Purpose</h4>
                    <br>
                    <p>
                        Saat ini JavaScript tidak hanya digunakan di sisi client (browser) saja lho. Semenjak adanya
                        Node.js JavaScript dapat digunakan di luar dari browser. Dengan begitu kamu bisa mengembangkan
                        back-end (server), console, program desktop, mobile, IoT, game, dan lainnya menggunakan
                        JavaScript.
                    </p>
                    <br>
                    <h4>2. Mudah untuk Dipelajari </h4>
                    <br>
                    <p>
                        Setiap bahasa pemrograman memiliki tingkat kesulitan yang berbeda. Tingkat kesulitan bisa
                        dilihat dari beberapa faktor. Menurut saya sih, syntax atau penulisan jadi salah satu faktor
                        yang paling penting buat dipelajari. JS masih tergolong mudah dipelajari asalkan kamu serius
                        dalam belajar dan selalu berani melakukan improvement atau bermain-main dengan itu.
